// sizeof operator ; it will show the size of and data-type.
#include<stdio.h>
int main(){
    int num;
    float salary;
    char ch;
    double marks;
    long lg;
    long double people;
    printf("\n Size of integer:%d",sizeof(num));
    printf("\n Size of float=%d",sizeof(salary));
    printf("\n Size of char=%d",sizeof(ch));
    printf("\n Size of long=%d",sizeof(lg));
    printf("\n Size of double=%d",sizeof(marks));
    printf("\n Size of long double=%d",sizeof(people));
}