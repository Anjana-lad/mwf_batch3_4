//assignment operator: +=,-=,/=,*=,%=,=
#include<stdio.h>
int main(){
    int num;
    printf("Enter number:");
    scanf("%d",&num);
        printf("Num=%d",num);
    num+=10;   // num=num+10
    printf("Num=%d",num);
    num-=50;  // num=num-50
        printf("Num=%d",num);
    num*=20; 
        printf("Num=%d",num);
    num/=2;
        printf("Num=%d",num);

}
